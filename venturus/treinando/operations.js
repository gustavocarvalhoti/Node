module.exports = {
    // Função chamada soma
    sum: (value1, value2) => {
        console.log(`Somando ${value1} + ${value2}`);
        return value1 + value2;
    },
    subtraction: (value1, value2) => {
        console.log(`Soubtraindo ${value1} - ${value2}`);
        return value1 - value2;
    }
};